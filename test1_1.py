# Код должен стать короче, количество строк должно уменьшиться.




dayOfWeek = ["понедельник", "вторник", "среда", "четверг", "пятница"] 

total = 0

for i in dayOfWeek:
    item = input(f'Введите расходы на продукты в {i}: ')
    total += int(item)

average = total / len(dayOfWeek)

print("Средняя сумма расходов на продукты в день:", average)

if 1 <= average <= 100:
    print("Малый расход")
elif 101 <= average <= 400:
    print("Средний расход")
elif average >= 401:
    print("Высокий расход")
else:
    print("Ничего не потрачено")
