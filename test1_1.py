    color = input('Введите цвет светофора, например: зеленый, желтый, красный. ')

    print(f'ваш ответ {color} принят')
    
    if color == "выход".lower():
        break
    elif "красный":
        print("стой!")
    elif "желтый":
        print("жди!")
    elif "зеленый":
        print("иди!")
